package com.senlainc.ignathick.Task3;

public class Corner {

    int x = 0;
    int y = 0;

    public Corner(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }


}
